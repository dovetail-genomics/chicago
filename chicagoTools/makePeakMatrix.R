	fetchcol <- scorecol
}else{
	if ((!twoPass & is.na(peaklistfile)) & (fetchcol != scorecol)){
		message("Fetchcol is different from scorecol, therefore the twoPass mode is switched on\n")
		twoPass <- TRUE
	}
